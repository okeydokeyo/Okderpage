            <td colspan="2">　　外傷性脊髓損傷之原因，以車禍、高處跌落、被重物壓傷為主，因此注重交通安全、職業安全等防止意外產生的安全守則，也是預防脊髓損傷發生之通則。在車禍或意外現場，受傷者一旦同時喪失運動與感覺機能，譬如下肢或頸部以下不能活動也沒有知覺，則要懷疑脊髓損傷的可能性。為了避免進一步人為傷害，在移動與轉送過程中，需要特別保護脊柱的穩定性。由於處理脊髓損傷需要有充份的設備與醫療人員，因此病人以轉送有經驗之醫學中心診治為宜。</td>
