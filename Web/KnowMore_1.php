            <td colspan="2">　　所謂「脊髓損傷」是指急性外傷性傷害侵及脊髓與神經，造成運動、感覺及大小便功能失常。這通常是由於巨大的外力，如車禍、墜落、重物壓傷、運動傷害等，使脊柱移位或骨折造成，而有些老年人發生損傷的原因是閃跌、滑交等小傷害。一般脊髓損傷都可發現其脊柱嚴重的破壞，但也有些損傷患者的影像檢查看不到脊柱明顯的變化，當然也有些人雖然傷及脊柱，但並未造成脊髓損傷。有些癱瘓的病人，並不是外傷性傷害造成，而是由於腫瘤、脊血管、發炎等因素，這些脊髓疾病的表現常與外傷性脊髓損傷相似。頸髓受傷會造成四肢癱瘓，約佔所有脊髓損傷患者的半數；若傷及胸髓、腰髓或薦髓，則會造成下半身癱瘓。除了運動與感覺機能障礙外，脊髓損傷還會造成排尿、排便困難及性功能障礙，而呼吸問題、自主神經機能異常也常見。不止如此，心理挫折、經濟問題等也跟著浮現。</td>
