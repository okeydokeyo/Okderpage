            <td colspan="2">運動傷害方面，歐美國家有多種劇烈運動而造成的脊髓損傷，如滑雪、足球、橄欖球等。在台灣則以跳水為最常見的脊髓損傷原因，多半是因為頭部撞到池底或其他東西而造成頸髓損傷。若能在跳水前訓練正確的技巧，並熟悉水底狀態，應能減少受傷的機會，或者謹守「不熟悉的場所不跳水」的原則，也是預防良策。最近愈來愈普及的水上遊樂場，常有滑水道的設施，請千萬不要以頭部向下的姿勢滑落，這種很「酷」的滑法，有很高的機會撞到防護欄或者池底，造成頸髓損傷或者更嚴重的腦傷，不可不慎。<br /><br /><br /></td>
