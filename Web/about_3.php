    <p>　　「新生命之家」於97年11月8日正式啟用，坐落於桃園縣楊梅鎮66號快速道路旁，是一棟專為脊髓損傷患友量身打造的無障礙建築。建築樓高四層，樓地板面積為960坪，新生命之家造價高達1億元（含土地、建築及設備），一磚一瓦全部都來自社會的捐獻及愛心。在新生命之家，無障礙設施並非附屬配置，而是建築的精神和主體，無論受傷程度如何，每一位傷友可以充分感受到完全的尊重和關懷，暫時排除外在環境的層層障礙，在這裡安心學習生活自理及訓練課程。<br>
<br>
　　甫進大廳，即可看到中心的願景：尊嚴、希望、新生命。這不僅是我們對每一位學員得深切期待，同時也成為中心服務的最高精神指標。全棟走廊皆設計成可以容納輪椅迴旋的寬度，房間均採拉門，讓傷友在輪椅上可以輕鬆開關，門檻完全無段差，方便輪椅進出。
<br>
新生命之家的廁所除了輪椅移動的空間外，所有的扶手與輔具，亦完全按照傷友需求所設置。寬大的電梯能夠容納數台輪椅共乘，透明門板則是為了在意外狀況發生時得以迅速處理。
<br>
<br>
發揮創意和用聲音服務的工作，是較適合傷友發展的。新生命之家一樓目前設置兩間職訓教室，供學員上課使用。中心常態開設的訓練班別有網頁設計班及客戶服務班。
二樓另設置兩間生活重建教室，除設有體能訓練器材維持傷友體能外，更設計多樣化教材，協助傷友學習移位、日常生活自理等技能。
<br>
<br>
中心所有平日用餐皆集中於二樓多功能視聽室，非用餐時段此處則成為大型會議室，中心同仁與學員之週會皆於此進行。三至四樓，則為學員宿舍，目前總計設有20間宿舍，可容納56人住宿。
