            <td>　　脊髓損傷好發於活動性強的年輕男性，16歲至30歲年齡層的患者佔總數一半以上，其中又以男性  居多佔80%。發生原因以車禍最為常見約佔50%，其次為由高處跌落或老人家跌倒，其他依次為運動傷害（跳水為主）、激烈鬥毆、槍傷等。以發生部位來看，頸髓是最好發的部位國外統計約佔50─60%，車禍、跳水、老人跌倒是主要原因。低胸椎及腰椎交界處是第二好發部位，從高處跌落及車禍是主要原因。其次是胸椎部位，被重物擊中、車禍、槍傷、刀傷是常見原因。人的脊髓只到第一腰椎處，因此第一腰椎以下損傷通常傷到的是馬尾束，馬尾束屬於周邊神經而不是像豆腐般脆弱的脊髓，其復原機會較大。胸椎處的神經管最狹窄，若剛受傷時之神經學檢查屬完全損傷，其復原機會是所有部位中最差的。頸椎部位之神經管相對的較寬，不完全損傷者可以有不錯的復原機會。<br /><br /><br /></td>
