            <td colspan="2">很多傷者在發生不幸的意外之後，由於現場急救處理不當，因而造成二度傷害，導致無法彌補的後果。所以，當意外發生時，首先要觀察傷者的脊髓是否受到傷害。如果傷者是清醒的，趕緊測試他的全身和四肢有沒有知覺，也就是請他的手指、腳趾頭動一動，如果不能動了，就表示他的頸髓可能已經受傷；如果下半身毫無知覺，就表示他的胸、腰髓可能受傷了。這時應儘快讓傷者躺平，身體不可彎曲，同時套上頸圈，保持他的頭、頸和軀幹成一直線，然後固定於搬運板或擔架上，迅速送醫急救。<br /></td>
