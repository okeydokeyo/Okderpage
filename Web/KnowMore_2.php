            <td>　　車禍是脊髓損傷最主要的原因，其中又以摩托車騎士面臨較大的危險。一般我們會呼籲騎乘摩托車者要戴安全帽，安全帽雖可預防比脊髓損傷更為可怕的腦傷，卻沒有預防脊髓損傷的效果。唯一的預防之道，只有安全駕駛，盡量避免車禍的發生。不論國內外，酒醉駕駛都是車禍的重要原因之一，「喝酒不開車，開車不喝酒」是所有駕駛人及乘客要確實遵守的最高原則。十個車禍九個快，在此要奉勸喜歡開快車或甚至飆車的朋友，好好愛惜自己的美好前程，不要為了逞一時之快，遺憾終身，連累了所有愛你的親友，傷了他們的心。<br /><br /><br /></td>
        </tr>
        <tr>
            <td>　　汽車車禍會傷及脊髓有兩種主要機制，一種是受到劇烈碰撞或緊急剎車時引起的頸部急遽前屈和後仰造成的頸髓損傷。綁安全帶及安全氣囊是非常重要的設施，可以降低衝撞力，減輕傷害程度。碰撞過程中若因緊張而頭部側轉，則容易造成頸椎移位而招致較嚴重的神經損傷。所以發生劇烈碰撞時最安全的姿勢是雙膝彎曲靠近胸部，頭部前彎兩手抱住頭頸部，減少頭部及脊椎受到太大的碰撞產生骨折或移位。較理想的安全帶是上下斜綁在軀幹的那種，若是攔腰橫綁的安全帶可能會造成腰椎的骨折而產生脊髓損傷。另一種受傷機制是身體碰撞車身或地面造成的胸腰椎交界處的損傷，同理若撞擊時伴隨身體旋轉的話，其受傷程度會較嚴重。彈起再摔落時，若以上肢或下肢撐地使得脊椎受力減少，則脊髓損傷程度可能較輕，但相對的常常伴隨有上肢或下肢之骨折。<br /><br /><br /></td>
        </tr>
        <tr>
            <td>　　跌倒所引起的脊髓損傷可分兩類，一類是從高處跌下引起的，多半是在高處工作摔下造成，部份是跳樓自殺不成留下的遺憾。其防範之道唯有注意工作場所的安全防護措施，避免工作人員從高處跌落。至於跳樓自殺多數發生在有心理挫折或精神疾病的人，需要其家人朋友提高警覺，而整個社會要注重心理健康及加強精神疾病者的保護及醫療。有不少高處跌落的脊髓損傷者是因爬到樹上摘水果跌落所造成的，不要小看爬樹，有時純粹為了好玩，也可能發生不幸的意外。<br /><br /><br /></td>
        </tr>
        <tr>
            <td>　　另一類與跌倒有關的脊髓損傷發生在中老年人，可能只是不小心在室內滑倒、坐在沙發上睡著跌下來、走樓梯不慎摔倒或是騎腳踏車跌倒，就造成嚴重的四肢麻痺。這是因為中老年人的頸椎常有骨刺增生，致使頸椎的神經管變狹窄，因此只是一個小小的外力，就造成柔軟如豆腐的頸髓產生血塊或水腫而有嚴重的神經症狀。通常病人並沒有明顯骨折脫位，一般在兩三個月內會有某種程度的神經復原，但由於病人的年紀較大，仍有可能造成非常大的行動障礙。預防之道是中年以後不宜從事高處攀爬的活動，尤其是平常不太熟悉的活動。有老年人的家庭，居家環境宜適度調整：例如走道照明應充足，地面要留意防滑（尤其是浴室，這是老年人滑倒最常發生的場所），不要有易被忽略的小障礙物留置地面。對有跌倒之虞的老人建議以柺杖助行，半夜不宜到廁所如廁，可改用房內之便桶，必要時在床邊加裝護欄防止睡夢中跌落床下。<br /><br /><br /></td>
        </tr>
